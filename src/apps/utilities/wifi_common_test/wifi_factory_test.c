{
    // uint16_t number = 2;
    // wifi_ap_record_t ap_info[2];
    uint16_t ap_count = 0;
    // memset(ap_info, 0, sizeof(ap_info));

    esp_wifi_scan_start(NULL, true);
    // ESP_ERROR_CHECK(esp_wifi_scan_get_ap_records(&number, ap_info));
    ESP_ERROR_CHECK(esp_wifi_scan_get_ap_num(&ap_count));
    ESP_LOGI(TAG, "Total APs scanned = %u", ap_count);

    _wifi_scan_num = (uint8_t)ap_count;


    _wifi_is_test_done = 1;
}

void wifi_factory_test(void)
{
    /* Data binding */
    _wifi_factory_test_info.ssid            = _wifi_ssid;
    _wifi_factory_test_info.password        = _wifi_password;
    _wifi_factory_test_info.ip              = _wifi_ip;
    _wifi_factory_test_info.is_connected    = &_wifi_is_connected;
    _wifi_factory_test_info.is_test_done    = &_wifi_is_test_done;
    _wifi_factory_test_info.scan_num        = &_wifi_scan_num;


    //Initialize NVS
    esp_err_t ret = nvs_flash_init();
    if (ret == ESP_ERR_NVS_NO_FREE_PAGES || ret == ESP_ERR_NVS_NEW_VERSION_FOUND) {
      ESP_ERROR_CHECK(nvs_flash_erase());
      ret = nvs_flash_init();
    }
    ESP_ERROR_CHECK(ret);

    ESP_LOGI(TAG, "ESP_WIFI_MODE_STA");
    wifi_init_sta();


    wifi_scan();
}


static void _wifi_factory_test_task(void* param)
{
    wifi_factory_test();
    ESP_LOGI(TAG, "task done");
    vTaskDelete(NULL);
}


void wifi_factory_test_start_task()
{
    xTaskCreate(_wifi_factory_test_task, "wifi", 5000, NULL, 15, NULL);
}

WiFiFactoryTestInfo_t* wifi_factory_test_get_info()
{
    return &_wifi_factory_test_info;
}
